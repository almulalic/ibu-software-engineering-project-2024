package ba.edu.ibu.eventport.api.core.model.enums;

/**
 * Enum representing the status of an event.
 * <p>
 * This enum includes various statuses such as creating, active, accepting invites, accepting guests,
 * approaching deadline, sold out, deadline passed, in progress, expired, and ended.
 */
public enum EventStatus {
  CREATING,
  ACTIVE,
  ACCEPTING_INVITES,
  ACCEPTING_GUESTS,
  APPROACHING_DEADLINE,
  SOLD_OUT,
  DEADLINE_PASSED,
  IN_PROGRESS,
  EXPIRED,
  ENDED
}
