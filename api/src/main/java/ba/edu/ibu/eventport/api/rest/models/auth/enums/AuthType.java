package ba.edu.ibu.eventport.api.rest.models.auth.enums;

/**
 * Enumeration representing different types of authentication methods.
 */
public enum AuthType {

  /**
   * Represents plain (default) authentication type.
   */
  PLAIN,

  /**
   * Represents Google authentication type.
   */
  GOOGLE,

  /**
   * Represents Microsoft authentication type.
   */
  MICROSOFT
}
